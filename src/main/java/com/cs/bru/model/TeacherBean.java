package com.cs.bru.model;

public class TeacherBean {
	private int teacherID;
	private String teacherUser;
	private String teacherPass;
	private String tacherName;
	private String teachLastName;
	private String baseClass;
	private String baseHour;
	private String position;
	private String positionTeach;
	public int getTeacherID() {
		return teacherID;
	}
	public void setTeacherID(int teacherID) {
		this.teacherID = teacherID;
	}
	
	public String getTeacherUser() {
		return teacherUser;
	}
	public void setTeacherUser(String teacherUser) {
		this.teacherUser = teacherUser;
	}
	public String getTeacherPass() {
		return teacherPass;
	}
	public void setTeacherPass(String teacherPass) {
		this.teacherPass = teacherPass;
	}
	public String getTacherName() {
		return tacherName;
	}
	public void setTacherName(String tacherName) {
		this.tacherName = tacherName;
	}
	public String getTeachLastName() {
		return teachLastName;
	}
	public void setTeachLastName(String teachLastName) {
		this.teachLastName = teachLastName;
	}
	public String getBaseClass() {
		return baseClass;
	}
	public void setBaseClass(String baseClass) {
		this.baseClass = baseClass;
	}
	public String getBaseHour() {
		return baseHour;
	}
	public void setBaseHour(String baseHour) {
		this.baseHour = baseHour;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getPositionTeach() {
		return positionTeach;
	}
	public void setPositionTeach(String positionTeach) {
		this.positionTeach = positionTeach;
	}
}
